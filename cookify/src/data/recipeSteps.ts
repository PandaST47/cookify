const STEPS_BY_ID: Record<string, RecipeStep[]> = {
    '1': CHEESE_SOUP,
}

/**
 * Возвращает шаги рецепта. Для рецептов без явных шагов генерирует
 * осмысленный fallback из ингредиентов, чтобы «Режим готовки»
 * работал везде. Чистая функция — безопасно звать в render/useMemo.
 */
export function getRecipeSteps(recipe: Recipe): RecipeStep[] {
    if (recipe.steps && recipe.steps.length > 0) return recipe.steps
    if (STEPS_BY_ID[recipe.id]) return STEPS_BY_ID[recipe.id]

    // Fallback: 4 обобщённых этапа на основе ингредиентов рецепта.
    const ings = recipe.ingredients.map((i) => i.name).join(', ')
    const cookSeconds = Math.max(5, Math.round(recipe.cookTime * 0.5)) * 60
    return [
        {
            text: `Подготовьте все ингредиенты: ${ings}. Помойте, очистите и нарежьте по необходимости.`,
            timerSeconds: 0,
            image: recipe.image,
            tip: 'Подготовьте всё заранее (mise en place) — готовить будет быстрее и спокойнее.',
        },
        {
            text: 'Разогрейте посуду (сковороду / кастрюлю / форму) и начните тепловую обработку основных ингредиентов согласно типу блюда.',
            timerSeconds: cookSeconds,
        },
        {
            text: 'Соедините компоненты, добавьте специи и доведите блюдо до готовности, пробуя на соль и баланс вкуса.',
            timerSeconds: Math.round(cookSeconds / 2),
            warning: 'Солите постепенно и пробуйте — досолить проще, чем исправить пересол.',
        },
        {
            text: `Подайте «${recipe.title}» к столу. Украсьте зеленью или гарниром по желанию. Приятного аппетита!`,
            timerSeconds: 0,
            image: recipe.image,
        },
    ]
}
